public class SwapTwoNosThirdVariable {
    public static void main(String[]args){
        int n1=10;
        int n2=30;
        int temp;
        System.out.println("Before swapping " +n1+" ,"+n2);

        temp=n1;//temp=10
        n1=n2;//n1=30
        n2=temp;//n2=10
        System.out.println("After swapping " +n1+" ,"+n2);


    }
    
}
